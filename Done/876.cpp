struct ListNode {
    int val;
    ListNode *next;
    ListNode(int x) : val(x), next(NULL) {}
};

class Solution {
public:
    ListNode* middleNode(ListNode* head) {
        ListNode* one = head;
        ListNode* two = head;
        while(two && two -> next) {
            two = two -> next;
            if (two) {
                two = two -> next;
            }
            one = one -> next;
        }
        return one;
    }
};

