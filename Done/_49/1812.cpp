class Solution {
public:
    bool squareIsWhite(string coordinates)
    {
        int idx1 = coordinates[0] - 'a';
        int idx2 = coordinates[1] - '0';
        if (idx1 % 2 == 0) {
            return idx2 % 2 == 0;
        } else {
            return idx2 % 2 == 1;
        }
    }
};

